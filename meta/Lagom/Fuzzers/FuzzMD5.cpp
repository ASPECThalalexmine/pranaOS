#include <libcrypto/hash/MD5.h>
#include <stddef.h>
#include <stdint.h>

extern "C" int LLVMFuzzerTestOneInput(const uint8_t* data, size_t size)
{
    auto buffer = ByteBuffer::copy(data, size);
    Crypto::Hash::MD5::hash(buffer);
    return 0;
}
